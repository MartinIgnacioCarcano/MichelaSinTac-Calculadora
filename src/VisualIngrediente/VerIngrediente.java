    private void guardarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBtnActionPerformed
        String nombre = textNombre.getText();
        double precio = Double.valueOf(textPrecio.getText());
        String aux = "";
        switch (precioPorUnidad.getSelectedIndex()) {
            case 0:
                aux = "k";
                precio = precio / 1000;
                break;
            case 1:
                aux = "k";
                precio = precio / 500;
                break;
            case 2:
                aux = "g";
                precio = precio / 100;
                break;
            case 3:
                aux = "l";
                precio = precio / 1000;
                break;
            case 4:
                aux = "c";
                break;
            case 5:
                aux = "u";
                break;
        }
        Ingrediente ingrediente = new Ingrediente();
        ingrediente.setId(idIngrediente);
        ingrediente.setNombre(nombre);
        ingrediente.setPrecioUnidad(precio);
        ingrediente.setUnidad(aux);
        if (idIngrediente == -10) {
            ic.crearIngrediente(ingrediente);
        } else {
            ic.actualizarIngrediente(ingrediente);
        }
        lista.actualizarDatos();
        this.dispose();
    }//GEN-LAST:event_guardarBtnActionPerformed

