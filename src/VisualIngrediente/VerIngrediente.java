    private void llenarPantalla() {
        Ingrediente ingrediente = ic.getElementById(idIngrediente);
        textNombre.setText(ingrediente.getNombre());
        double precio = ingrediente.getPrecioUnidad();
        String aux = ingrediente.getUnidad();
        switch (aux) {
            case "k":
                precioPorUnidad.setSelectedIndex(0);
                precio = precio * 1000;
                break;
            case "g":
                precioPorUnidad.setSelectedIndex(2);
                precio = precio * 100;
                break;
            case "l":
                precioPorUnidad.setSelectedIndex(3);
                precio = precio * 1000;
                break;
            case "c":
                precioPorUnidad.setSelectedIndex(4);
                break;
            case "u":
                precioPorUnidad.setSelectedIndex(5);
                break;
        }
        textPrecio.setText(String.valueOf(precio));
    }
